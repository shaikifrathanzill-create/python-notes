# Fruits
fruits=['apple','banana','mango']

#Adding an Element
fruits.append('orange')
print(fruits)

#Remove an element from the list
fruits=['apple','banana','mango','grapes']
fruits.remove('banana')
print(fruits)

#Changing an element
fruits [1]='grapes'
print(fruits)

#Access elements from a list
fruits=['apple','banana','mango','grapes','orange']
print(fruits[0])
print(fruits[-1])

#Sort the list
numbers=[5,3,2,7,8]
numbers.sort()
print(numbers)